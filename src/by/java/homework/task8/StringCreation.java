        public String insertSubstringToText (int k, String text) {
            String textInsert = "--Insert--";
            String[] words = text.split(",", k);
            StringBuilder newText = new StringBuilder();
            for (int s = 0; s < words.length; s++) {
                if (s < words.length - 1) {
                    String partResult = words[s].substring(0, k) + textInsert + words[s].substring(k) + ",";
                    newText.append(partResult);
                } else {
                    String partResult = words[s].substring(0, k) + textInsert + words[s].substring(k);
                    newText.append(partResult);
                }
            }
            return String.valueOf(newText);
        }

